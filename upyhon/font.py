class FontRenderer:
    
    def __init__(self, width, height, pixel, font_name='font5x8.bin'):
        # Specify the drawing area width and height, and the pixel function to
        # call when drawing pixels (should take an x and y param at least).
        # Optionally specify font_name to override the font file to use (default
        # is font5x8.bin).
        self._width = width
        self._height = height
        self._pixel = pixel
        self._font_name = font_name
        self._cursor_pos = 0

    def init(self):
        # Open the font file and grab the character width and height values.
        # Note that only fonts up to 8 pixels tall are currently supported.
        self._font = FONT
        self._font_width = 5
        self._font_height = 8

 

    def __enter__(self):
        self.init()
        return self


    def draw_char0(self, ch, x, y, *args, **kwargs):
        # Don't draw the character if it will be clipped off the visible area.
        if x < -self._font_width or x >= self._width or \
           y < -self._font_height or y >= self._height:
            return
        # Go through each column of the character.
        for char_x in range(self._font_width):
            # Grab the byte for the current column of font data.
            #self._font.seek(2 + (ord(ch) * self._font_width) + char_x)
            #line = ustruct.unpack('B', self._font.read(1))[0]
            line_pos=2 + (ord(ch) * self._font_width) + char_x
            line = self._font[line_pos]
            # Go through each row in the column byte.
            for char_y in range(self._font_height):
                # Draw a pixel for each bit that's flipped on.
                if (line >> char_y) & 0x1:
                    self._pixel(x + char_x, y + char_y, *args, **kwargs)


    def draw_char(self, ch, x, y, color):
        if ch==' ':
              return 3
        maxX=0
        # Don't draw the character if it will be clipped off the visible area.
        if x < -self._font_width or x >= self._width or \
           y < -self._font_height or y >= self._height:
            return maxX
        # Go through each column of the character.
        xx=0
        for char_x in range(self._font_width):
            line_pos= (ord(ch) * self._font_width) + char_x
            line = self._font[line_pos]
            if ch in ('4','3','2','7','5','6','8','0','9') and char_x==2:
                continue
            if ch in ('1') and char_x==0:
                continue
            # Go through each row in the column byte.
            if ch in ('7') and xx==0:
                line=line&1
            if ch in ('7') and xx==1:
                line=line|64|16
            for char_y in range(self._font_height):
                # Draw a pixel for each bit that's flipped on.
                if ch in ('6') and char_y==0 and xx==3:
                    continue
                if ch in ('9') and char_y==6 and xx==0:
                    continue
                if ch in ('0') and ((char_y==2 and xx==2) or (char_y==4 and xx==1)):
                    continue
                
                if (line >> char_y) & 0x1:
                    if maxX<xx:
                       maxX=xx
                    self._pixel(x + xx, y + char_y, color)
            xx=xx+1        
        return maxX

    def text0(self, text, x, y, *args, **kwargs):
        # Draw the specified text at the specified location.
        for i in range(len(text)):
            self.draw_char(text[i], x + (i * (self._font_width + 1)), y,
                           *args, **kwargs)

    def text0(self, text, x, y, color):
        # Draw the specified text at the specified location.
        self._cursor_pos=x
        
        for i in range(len(text)):
            cl=color
            if (text[i]==',' or text[i]=='.') and i>0:
                self._cursor_pos-=1
            if text[i]==',' or text[i]=='.':
                    cl=(15-color[0],15-color[1],15-color[2])
            maxX=self.draw_char(text[i], self._cursor_pos , y, cl)
            self._cursor_pos+=(maxX+1)

    def text(self, text, x, y, color):
        self._cursor_pos=x
        if type(color) is tuple and len(color)==3 and type(color[0]) is int and type(color[1]) is int and type(color[2]) is int:
            self.text0(text, x, y, color)
        else:
            for w in range(len(text.split(' '))):
                clw=color[0]
                if w>0:
                    self._cursor_pos +=1 #space
                if w+1>len(color):
                    clw=color[len(color)-1]
                else:
                    clw=color[w]
                word=text.split(' ')[w]    
                for i in range(len(word)):
                    cl=clw
                    if word[i]==',' or word[i]=='.':
                        cl=(15-clw[0],15-clw[1],15-clw[2])
                        if i>0:
                            self._cursor_pos-=1
                    maxX=self.draw_char(word[i], self._cursor_pos , y, cl)
                    self._cursor_pos+=(maxX+1)



